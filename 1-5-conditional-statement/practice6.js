
let food = '피자';

switch (food) {
  case '피자':
    console.log('이탈리아 요리입니다.');
    break;
  case '타코':
    console.log('멕시코 요리입니다.');
    break;
  case '스시':
    console.log('일본 요리입니다.');
    break;
  case '바게트':
    console.log('프랑스 요리입니다.');
    break;
  default:
    console.log('해당 음식에 대한 정보가 없습니다.');
}